# crie um algoritimo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

numero = int(input('qual o seu numero?:'))

dobro = numero *2
triplo = numero *3
raizQuadrada = numero **(1/2)

print('o dobro de {} é {} e o triplo é {} e a raiz quadrada é {}'.format(numero, dobro, triplo, raizQuadrada))